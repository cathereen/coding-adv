d_chunk = { "(": -3, "[": -57, "{": -1197, "<": -25137, ")": 3, "]": 57, "}": 1197, ">": 25137 }
open_char = ["(", "[", "{", "<"]
close_char = [")", "]", "}", ">"]
close_point = {")": 1, "]": 2, "}": 3, ">": 4}

error_points = 0

def illegal_char(val):
    global error_points
    error_points += val

def main():
    completion_score=[]
    keys=list(d_chunk.keys())
    values=list(d_chunk.values())
    for line in input_string.splitlines():
        stack = []
        isCorrupt = False
        total_score = 0
        for key in line:
            val = d_chunk.get(key)
            if val < 0: stack.append(val) 
            if val > 0:
                if not stack:
                    ch = 0 
                else:
                    ch = stack.pop()
                if (ch + val) != 0 :
                    isCorrupt = True
                    illegal_char(val)
                    #print("Illegal character " + key + " encountered in line " + line)
                    break
        if not isCorrupt:
            complete_seq = ""
            while len(stack) != 0:
                ch = close_char[open_char.index(keys[values.index(stack.pop())])]
                if ch!= None: 
                    complete_seq += ch
                    total_score = total_score * 5 + close_point.get(ch)
            completion_score.append(total_score)
            print("Completion sequences for line " + line + "is " + complete_seq + "and score is ", total_score)
    completion_score.sort()
    print("Middle score is: ", completion_score[int(len(completion_score)/2)])
    print("The total syntax error score: ", error_points)

if __name__ == "__main__":
    main()
